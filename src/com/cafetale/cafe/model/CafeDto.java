package com.cafetale.cafe.model;

public class CafeDto {
	String cafe_id;
	String cafe_title;
	String cafe_newAddress;
	String cafe_zipcode;
	String cafe_phone;

	public String getCafe_id() {
		return cafe_id;
	}
	public void setCafe_id(String cafe_id) {
		this.cafe_id = cafe_id;
	}
	public String getCafe_title() {
		return cafe_title;
	}
	public void setCafe_title(String cafe_title) {
		this.cafe_title = cafe_title;
	}
	public String getCafe_newAddress() {
		return cafe_newAddress;
	}
	public void setCafe_newAddress(String cafe_newAddress) {
		this.cafe_newAddress = cafe_newAddress;
	}
	public String getCafe_zipcode() {
		return cafe_zipcode;
	}
	public void setCafe_zipcode(String cafe_zipcode) {
		this.cafe_zipcode = cafe_zipcode;
	}
	public String getCafe_phone() {
		return cafe_phone;
	}
	public void setCafe_phone(String cafe_phone) {
		this.cafe_phone = cafe_phone;
	}	
}
